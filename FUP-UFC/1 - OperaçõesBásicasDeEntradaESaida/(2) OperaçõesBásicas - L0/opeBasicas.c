#include <stdio.h>

int main(){
    
    int n1;
    int n2;
    
    scanf("%d", &n1);
    scanf("%d", &n2);
    
    printf("%d\n", n1 + n2); // Soma
    printf("%d\n", n1 - n2); // Subtração
    printf("%d\n", n2 * n1); // Mutiplicação
    printf("%d", n1 % n2); // Resto
    
    return 0;
    
}
